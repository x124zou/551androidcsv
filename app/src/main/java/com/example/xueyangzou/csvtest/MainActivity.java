    public String read(){

        String next[] = {};
        List<String[]> list = new ArrayList<String[]>();
        try {
            CSVReader reader = new CSVReader(new InputStreamReader(getAssets().open("asian.csv")));//Specify asset file name
//in open();
            int i =0;
            for(;;) {
                next = reader.readNext();
                if(next != null) {
                    list.add(next);
                } else {
                    break;
                }

                Log.d("list", list.get(i)[1]);
                i++;
            }
        } catch (IOException e) {
            e.printStackTrace();}

        return list.get(0)[1];

    }


